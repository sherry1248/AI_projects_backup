HUMAN_LIKE_MAX_RAW_SCORE = sum(
    config["weight"] * 10 for config in HUMAN_LIKE_SCORE_DIMENSIONS.values()
)
HUMAN_LIKE_MAX_PASSABLE_AI_NESS_PENALTY = 9


HUMAN_LIKE_VERDICT_RULE = (
    "仅当 overall_score >= 75、naturalness >= 6、empathy >= 6，且 ai_ness_penalty <= 9（即没有达到严重机器感区间）时，verdict 才能给 YES。"
)


def format_human_like_score_anchors() -> str:
    """Format dimension anchor rules into readable Chinese text for the judging prompt."""
    lines: List[str] = []
    for key, config in HUMAN_LIKE_SCORE_DIMENSIONS.items():
        lines.append(f"- {key}：{config['description']}")
        anchors = config.get("anchors", {})
        for score_range, description in anchors.items():
            lines.append(f"  - {score_range} 分：{description}")

    lines.append("- ai_ness_penalty：机器感惩罚分")
    for score_range, description in HUMAN_LIKE_AI_NESS_PENALTY_ANCHORS.items():
        lines.append(f"  - {score_range} 分：{description}")
    return "\n".join(lines)


